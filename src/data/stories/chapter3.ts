// ==================== 类型定义 ====================

export interface StoryChoice {
  id: string;
  text: string;
  nextNode?: string;
  next?: string;
  effects?: StoryEffect[];
  conditions?: StoryCondition[];
  condition?: StoryCondition;
}

export interface StoryCondition {
  type: 'flag' | 'affection' | 'choice' | 'chapter' | 'hasCard' | 'hasItem' | 'all_affections_min';
  key?: string;
  target?: string | string[];
  targets?: string[];
  value?: boolean | number | string;
  min?: number;
  characterId?: string;
  operator?: 'eq' | 'gt' | 'gte' | 'lt' | 'lte';
}

export interface StoryEffect {
  type: 'affection' | 'flag' | 'stat' | 'item' | 'unlock' | 'cg_unlock' | 'title_unlock' | 'item_unlock' | 'achievement' | 'ending' | 'choice' | 'setFlag' | 'addCard' | 'removeCard' | 'unlockCG' | 'addItem';
  key?: string;
  target?: string;
  value?: number | string | boolean;
  characterId?: string;
  amount?: number;
}

export interface CharacterSprite {
  characterId: string;
  position: 'left' | 'center' | 'right' | 'far-left' | 'far-right' | 'mid-left' | 'mid-right' | 'center-left' | 'center-right';
  emotion: string;
  outfit?: string;
}

export interface StoryNode {
  id: string;
  type: 'dialogue' | 'choice' | 'cutscene' | 'branch' | 'battle';
  chapter: number;
  speaker?: string;
  text: string;
  emotion?: string;
  portrait?: string;
  background?: string;
  bgm?: string;
  music?: string;
  next?: string;
  nextNode?: string;
  enemyId?: string;
  defaultOption?: StoryChoice;
  choices?: StoryChoice[];
  effects?: StoryEffect[];
  flags?: Record<string, boolean>;
  characterSprites?: CharacterSprite[];
}

export interface ChapterInfo {
  id: number;
  title: string;
  subtitle: string;
  description: string;
  parts: {
    id: string;
    title: string;
    startNode: string;
    nodeCount: number;
  }[];
  mainCharacters: string[];
  backgrounds: string[];
  bgmList: string[];
  totalNodes: number;
  totalChoices: number;
}

